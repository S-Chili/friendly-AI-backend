const getResponse = (message) => {
  const lowerMsg = message.toLowerCase();

  for (const category in categories) {
    if (categories[category].some((keyword) => lowerMsg.includes(keyword))) {
      let reply =
        responses[category][
          Math.floor(Math.random() * responses[category].length)
        ];

      // –î–æ–¥–∞—î–º–æ —á–∞—Å–æ–≤—ñ –ø—Ä–∏–≤—ñ—Ç–∞–Ω–Ω—è –¥–æ –∫–∞—Ç–µ–≥–æ—Ä—ñ—ó "greetings"
      if (category === "greetings") {
        reply = `${getTimeBasedGreeting()} ${reply}`;
      }

      return reply;
    }
  }

  return responses.default[
    Math.floor(Math.random() * responses.default.length)
  ];
};

